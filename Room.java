public class Room
{

	private String no;
	private String type;
	private boolean isUse;
	public void setNo(String no){
		this.no = no;
	}
	public String getNo(){
		return no;
	}
	public void setType(String type){
		this.type = type;
	}
	public String getType(){
		return type;
	}
	public void setIsUse(boolean isUse){
		this.isUse = isUse;
	}
	public boolean getIsUse(){
		return isUse;
	}	
}